package com.tnsif.day3;

public class ForEachLoop {

	public static void main(String[] args) {
		int a[]= {10,20,30,40,50};
		//for integer array
		for(int i:a)
		{
			System.out.println(i);
		}
		float f1[]= {1.2f,1.4f,2.5f};
		for(float f:f1)
		{
			System.out.println(f);
		}

	}

}
